// static memory allocation  
//  - Compile time allocation
//  - Memory is allocated,deallocated at compile time
//  - Memory is allocated on stack
//  - arrays are satic
//  - we cant resize the size of array



// dynamic memory allocation
//  - Runtime allocation
//  - Memory is allocated,deallocated at runtime
//  - Memory is allocated on heap
//  - vectors are used for dynamic memory allocation
//  - we can resize the size of vector



// capacity of a vector:
//  - The maximum number of elements a vector can hold
//  - It is the size of the memory block allocated for the vector 
//  - if the capacity is full, it will double the capacity and reallocate the memory
//  - if the capacity is not full, it will not change the capacity